int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	cons;

	i = 0;
	cons = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			cons = s1[i] - s2[i];
			break ;
		}
		i++;
	}
	return (cons);
}
