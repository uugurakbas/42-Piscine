int	ft_str_is_numeric(char *str)
{
	int	i;
	int	t_f;

	i = 0;
	t_f = 1;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			t_f = 1;
		else
		{
			t_f = 0;
			break ;
		}
		i++;
	}
	return (t_f);
}
